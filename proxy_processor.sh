==================================================
          Скрипт обработки прокси-серверов         
==================================================

Введите формат вывода прокси, используя следующие переменные:
  - log   : Логин (username)
  - pass  : Пароль (password)
  - ip    : IP-адрес прокси
  - port  : Порт прокси

Примеры форматов вывода:
  1. С аутентификацией (логин и пароль):
     log:pass@ip:port
     Пример: user1:password1@192.168.1.1:8080

  2. Без аутентификации:
     ip:port
     Пример: 192.168.1.1:8080

Введите формат вывода и пример (например, log:pass@ip:port):
Формат вывода: log:pass@ip:port

Выбранный формат: log:pass@ip:port

Вставьте ваши прокси, по одному в строке.
Примеры прокси с аутентификацией и без неё:
  user1:password1@192.168.1.1:8080
  192.168.1.2:3128

Когда закончите, нажмите Ctrl+D (EOF).
user1:password1@192.168.1.1:8080
user2:password2@10.0.0.2:3128
192.168.1.3:8000
invalid_proxy_line
^D
Предупреждение: Некорректный формат прокси 'invalid_proxy_line'. Пропускаем.

Переработанные прокси:
-----------------------
user1:password1@192.168.1.1:8080
user2:password2@10.0.0.2:3128
192.168.1.3:8000

Прокси сохранены в файл 'output_proxies.txt'
